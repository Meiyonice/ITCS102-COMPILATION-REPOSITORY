# ASSIGNMENT OPERATORS
# | = | += | -= | *= | /= | %= | //= | **= |
# Create a program that will assign 10 to the value of x.

x = 10
print(x)

# Add an additional 10 to the current of x.
# (+= ) - Incrementer
x += 10
print(x)

# Add an additional 20 to the current of x.
x += 20
print(x)

# Lastly, multiply the current of x to 2.
x *= 2
print(x)